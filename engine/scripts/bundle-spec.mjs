import { cpSync, mkdirSync, rmSync, writeFileSync, readdirSync, readFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { computeSpecHash } from "./compute-spec-hash.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const SPEC_SRC = join(ROOT, "..", "spec");
const SPEC_DST = join(ROOT, "dist", "spec");

rmSync(SPEC_DST, { recursive: true, force: true });
mkdirSync(SPEC_DST, { recursive: true });

// cpSync handles missing source dirs poorly — guard with a directory listing.
const subdirs = readdirSync(SPEC_SRC, { withFileTypes: true });
for (const entry of subdirs) {
  const src = join(SPEC_SRC, entry.name);
  const dst = join(SPEC_DST, entry.name);
  if (entry.isDirectory()) {
    cpSync(src, dst, { recursive: true });
  } else {
    cpSync(src, dst);
  }
}

const hash = computeSpecHash(SPEC_DST);

const generated = `// AUTO-GENERATED by scripts/bundle-spec.mjs — do not edit.
export const SPEC_HASH = ${JSON.stringify(hash)};
`;
writeFileSync(join(ROOT, "src", "spec-hash.ts"), generated);

// ─── Generate inlined-spec source (browser-compatible) ────────────────────────
// Load all schemas from spec/schemas/
const SCHEMAS_DIR = join(SPEC_SRC, "schemas");
const schemaFiles = readdirSync(SCHEMAS_DIR).filter(f => f.endsWith(".schema.json"));
const schemasObj = {};
for (const file of schemaFiles) {
  const key = file.replace(".schema.json", "");
  schemasObj[key] = JSON.parse(readFileSync(join(SCHEMAS_DIR, file), "utf8"));
}

// Load lifecycles
const LIFECYCLES_DIR = join(SPEC_SRC, "lifecycles");
const lifecycleFiles = readdirSync(LIFECYCLES_DIR).filter(f => f.endsWith(".lifecycle.json"));
const lifecyclesObj = {};
for (const file of lifecycleFiles) {
  const key = file.replace(".lifecycle.json", "");
  lifecyclesObj[key] = JSON.parse(readFileSync(join(LIFECYCLES_DIR, file), "utf8"));
}

// Load registries
const REGISTRIES_DIR = join(SPEC_SRC, "registries");
const registryFiles = readdirSync(REGISTRIES_DIR).filter(f => f.endsWith(".json"));
const registriesObj = {};
for (const file of registryFiles) {
  const key = file.replace(".json", "");
  registriesObj[key] = JSON.parse(readFileSync(join(REGISTRIES_DIR, file), "utf8"));
}

const generatedSpec = `// AUTO-GENERATED by scripts/bundle-spec.mjs — do not edit.

import type { SchemaName } from "./types.js";

export const SCHEMAS: Record<SchemaName, unknown> = ${JSON.stringify(schemasObj, null, 2)};

export const LIFECYCLES = ${JSON.stringify(lifecyclesObj, null, 2)} as const;

export const REGISTRIES = ${JSON.stringify(registriesObj, null, 2)} as const;
`;
writeFileSync(join(ROOT, "src", "generated-spec.ts"), generatedSpec);

console.log(`bundled spec/ → dist/spec/, SPEC_HASH=${hash}`);
console.log(`generated src/generated-spec.ts (schemas: ${Object.keys(schemasObj).length}, lifecycles: ${Object.keys(lifecyclesObj).length}, registries: ${Object.keys(registriesObj).length})`);
