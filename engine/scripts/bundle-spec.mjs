import { cpSync, mkdirSync, rmSync, writeFileSync, readdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { computeSpecHash } from "./compute-spec-hash.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const SPEC_SRC = join(ROOT, "..", "spec");
const SPEC_DST = join(ROOT, "dist", "spec");

rmSync(SPEC_DST, { recursive: true, force: true });
mkdirSync(SPEC_DST, { recursive: true });

// cpSync handles missing source dirs poorly — guard with a directory listing.
const subdirs = readdirSync(SPEC_SRC, { withFileTypes: true });
for (const entry of subdirs) {
  const src = join(SPEC_SRC, entry.name);
  const dst = join(SPEC_DST, entry.name);
  if (entry.isDirectory()) {
    cpSync(src, dst, { recursive: true });
  } else {
    cpSync(src, dst);
  }
}

const hash = computeSpecHash(SPEC_DST);

const generated = `// AUTO-GENERATED by scripts/bundle-spec.mjs — do not edit.
export const SPEC_HASH = ${JSON.stringify(hash)};
`;
writeFileSync(join(ROOT, "src", "spec-hash.ts"), generated);

console.log(`bundled spec/ → dist/spec/, SPEC_HASH=${hash}`);
